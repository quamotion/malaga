namespace Quamotion.Malaga
{
    /// <summary>
    /// Represents a physical button on an iOS device.
    /// </summary>
    public enum DeviceButton
    {
        /// <summary>
        /// The device's home button.
        /// </summary>
        Home = 1,

        /// <summary>
        /// The device's volume up button.
        /// </summary>
        VolumeUp = 2,

        /// <summary>
        /// The device's volume down button.
        /// </summary>
        VolumeDown = 3
    }
}
